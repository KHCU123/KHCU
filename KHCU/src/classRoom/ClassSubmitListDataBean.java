package classRoom;

public class ClassSubmitListDataBean {
	private String attendBookName;
	private String classSubmitHw;
	private String classcode;
	private String stNum;
	private String stName;
	private String type;
	private String hwscore;
	private String submit_name;
	private String submit_path;
	
	
	public String getClasscode() {
		return classcode;
	}
	public void setClasscode(String classcode) {
		this.classcode = classcode;
	}
	public String getAttendBookName() {
		return attendBookName;
	}
	public void setAttendBookName(String attendBookName) {
		this.attendBookName = attendBookName;
	}
	public String getClassSubmitHw() {
		return classSubmitHw;
	}
	public void setClassSubmitHw(String classSubmitHw) {
		this.classSubmitHw = classSubmitHw;
	}
	public String getStNum() {
		return stNum;
	}
	public void setStNum(String stNum) {
		this.stNum = stNum;
	}
	public String getStName() {
		return stName;
	}
	public void setStName(String stName) {
		this.stName = stName;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}

	public String getHwscore() {
		return hwscore;
	}
	public void setHwscore(String hwscore) {
		this.hwscore = hwscore;
	}
	public String getSubmit_name() {
		return submit_name;
	}
	public void setSubmit_name(String submit_name) {
		this.submit_name = submit_name;
	}
	public String getSubmit_path() {
		return submit_path;
	}
	public void setSubmit_path(String submit_path) {
		this.submit_path = submit_path;
	}
	
	
}
